# Файл для работы с товарами

def process_merch():
    print("Доступные товары: футболка — 500 руб, кружка — 300 руб")
    item = input("Что хочешь купить? (футболка/кружка): ")
    prices = {"футболка": 500, "кружка": 300}
    if item in prices:
        print(f"Сумма к оплате: {prices[item]} руб")
    else:
        print("Такого товара нет, братишка!")
